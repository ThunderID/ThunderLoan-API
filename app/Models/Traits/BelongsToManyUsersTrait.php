<?php 

namespace App;

use Illuminate\Support\MessageBag;

trait BelongsToManyUsersTrait {

	static function bootBelongsToManyUsersTrait()
	{

	}

	// --------------------------------------------------------------------
	// RELATIONS
	// --------------------------------------------------------------------
	public function users()
	{
		return $this->belongsToMany(__NAMESPACE__ . '\User');
	}

	// --------------------------------------------------------------------
	// SCOPE
	// --------------------------------------------------------------------
	public function scopeBelongsToUserId($q, $v)
	{
		if (!$v)
		{
			return $q;
		}
		else
		{
			return $q->whereHas('users', function($q) use ($v) {
				$q->whereIn('id', is_array($v) ? $v : [$v]);
			});
		}
	}


	// --------------------------------------------------------------------
	// ACCESSOR
	// --------------------------------------------------------------------

	// --------------------------------------------------------------------
	// MUTATOR
	// --------------------------------------------------------------------

	// --------------------------------------------------------------------
	// FUNCTIONS
	// --------------------------------------------------------------------
}
