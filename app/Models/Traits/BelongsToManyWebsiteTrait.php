<?php 

namespace App;

use Illuminate\Support\MessageBag;

trait BelongsToManyWebsitesTrait {

	static function bootBelongsToManyWebsitesTrait()
	{

	}

	// --------------------------------------------------------------------
	// RELATIONS
	// --------------------------------------------------------------------
	public function websites()
	{
		return $this->belongsTo(__NAMESPACE__ . '\Website');
	}

	// --------------------------------------------------------------------
	// SCOPE
	// --------------------------------------------------------------------
	public function scopeBelongsToWebsiteId($q, $v)
	{
		if (!$v)
		{
			return $q;
		}
		else
		{
			return $q->whereHas('websites', function($q) use ($v) {
				$q->whereIn('id', is_array($v) ? $v : [$v]);
			});
		}
	}

	// --------------------------------------------------------------------
	// ACCESSOR
	// --------------------------------------------------------------------

	// --------------------------------------------------------------------
	// MUTATOR
	// --------------------------------------------------------------------

	// --------------------------------------------------------------------
	// FUNCTIONS
	// --------------------------------------------------------------------
}
